/**
  * add - addition
  * @a: number
  * @b: number
  * Return: result
  */
int add(int a, int b)
{
	return (a + b);
}
/**
  * sub - subtraction
  * @a: number to be subtracted from
  * @b: number to subtract
  * Return: subtaction of a and b
  */
int sub(int a, int b)
{
	return (a - b);
}
/**
  * mul - multiplication
  * @a: number to multiply
  * @b: number to multiply
  * Return: multiplication of a and b
  */
int mul(int a, int b)
{
	return (a * b);
}
/**
  * div - division
  * @a: number to divide
  * @b: number to be divided with
  * Return: division of a and b
  */
int div(int a, int b)
{
	return (a/ b);
}
/**
  * mod - modulus
  * @a: number to get reminder from
  * @b: number to divide with
  * Return: modulus of a and b
  */
int mod(int a, int b)
{
	return (a % b);
}
